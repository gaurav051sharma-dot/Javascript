// While Loop

let i=1
while(i <= 10){
    // console.log(`Value of i is ${i}`)
    i=i+2    // i+=2
}

let myArray=["Flash","Batman","Superman"]

let j=0
while(j < myArray.length){
    // console.log(`Value is ${myArray[j]}`)
    j=j+1    // j+=1   j++
}

// Do-While Loop

let k=1
do {
    // console.log(`Value of k is ${k}`)
    k++
} while (k<=10)

let m=11
do {
    console.log(`Value of m is ${m}`)
    m++
} while (m<=10)

// do-while loop is executed atLeast once even if the condition is false